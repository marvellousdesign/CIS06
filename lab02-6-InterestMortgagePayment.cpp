#include <iostream>
#include <cmath>
#include <iomanip>
using namespace std;

int main() {
	double principal, yearlyRate, years, monthlyRate, monthlyPayment, interests, amtToPrincipal, endPrincipal;
	int month = 1;

	cout << "Please enter the principal, yearly rate(interger), & the number of years: ";
	cin >> principal >> yearlyRate >> years;
	cout << endl;

	monthlyRate = (yearlyRate / 100) / 12;

	monthlyPayment = principal * monthlyRate / (1 - (1 / pow(1 + monthlyRate, years * 12)));

	cout << setw(7) << "Month" << setw(17) << "Monthly Payment" << setw(12) << "Interests"
		<< setw(22) << "Amount to Principal" << setw(20) << "End to Principal" << endl;
	cout << "--------------------------------------------------------------------------------" << endl;

	for (int i = 0; i < 36; i++) {
		interests = principal * (yearlyRate / 100) / 12;

		amtToPrincipal = monthlyPayment - interests;

		if (principal < amtToPrincipal) {
			endPrincipal =  amtToPrincipal - amtToPrincipal;
		} else {
			endPrincipal = principal - amtToPrincipal;
		}

		cout << setw(5) << month++ << setw(15) << monthlyPayment << setw(15)
			<< interests << setw(17) << amtToPrincipal << setw(21) << endPrincipal << endl;

		principal = endPrincipal;
	}
	return 0;
}

